const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  powerof: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(7, 10);
calculator.minus(7, 10);
calculator.divide(7, 10);
calculator.powerof(7, 10);
